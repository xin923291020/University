package test1;

public class Computer
{
	public String CPU = "";
	public String RAM = "";
	public String HD = "";
	public String MB = "";
	public void setCPU(String cpu)
	{
		CPU = cpu;
	}
	public String getCPU()
	{
		return CPU;
	}
	public void setRAM(String ram)
	{
		RAM = ram;
	}
	public String getRAM()
	{
		return RAM;
	}
	public void setHD(String hd)
	{
		HD = hd;
	}
	public String getHD()
	{
		return HD;
	}
	public void setMB(String mb)
	{
		MB = mb;
	}
	public String getMB()
	{
		return MB;
	}
}